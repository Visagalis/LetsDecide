var currentLang = localStorage.getItem('ld-lang') ||
    (navigator.language && navigator.language.startsWith('lt') ? 'lt' : 'en');

function applyLang(lang) {
    currentLang = lang;
    localStorage.setItem('ld-lang', lang);
    document.documentElement.lang = lang;
    document.getElementById('lang-label').textContent = lang === 'en' ? 'LT' : 'EN';

    var dict = T[lang];
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
        var key = el.getAttribute('data-i18n');
        if (dict[key] !== undefined) el.textContent = dict[key];
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
        var key = el.getAttribute('data-i18n-html');
        if (dict[key] !== undefined) el.innerHTML = dict[key];
    });

    document.title = lang === 'en'
        ? 'LetsDecide \u2014 AI-Assisted Group Decisions'
        : 'LetsDecide \u2014 DI padedami grupės sprendimai';
}

function toggleLang() {
    applyLang(currentLang === 'en' ? 'lt' : 'en');
}

applyLang(currentLang);
