var currentLang = getPreferredLang();

function getPreferredLang() {
    try {
        var requested = new URLSearchParams(window.location.search).get('lang');
        if (requested === 'en' || requested === 'lt') return requested;
    } catch (error) {
        /* URLSearchParams can be unavailable in very old browsers. */
    }

    var stored = null;
    try {
        stored = localStorage.getItem('ld-lang');
    } catch (error) {
        stored = null;
    }
    if (stored === 'en' || stored === 'lt') return stored;
    return navigator.language && navigator.language.toLowerCase().startsWith('lt') ? 'lt' : 'en';
}

function storeLang(lang) {
    try {
        localStorage.setItem('ld-lang', lang);
    } catch (error) {
        /* localStorage can be unavailable in strict privacy modes. */
    }
}

function applyText(selector, attr, setter, dict) {
    document.querySelectorAll(selector).forEach(function (el) {
        var key = el.getAttribute(attr);
        if (dict[key] === undefined) return;
        setter(el, dict[key]);
    });
}

function getActiveMode() {
    var active = document.querySelector('.mode-tab.active');
    return active && active.dataset.mode ? active.dataset.mode : 'overview';
}

function updateModeLabel(mode) {
    var label = document.getElementById('mode-label');
    if (!label || !T[currentLang]) return;
    var suffix = mode.charAt(0).toUpperCase() + mode.slice(1);
    var key = 'ai.demo' + suffix;
    label.textContent = T[currentLang][key] || T[currentLang]["ai.demoOverview"];
}

function applyLang(lang) {
    currentLang = lang === 'lt' ? 'lt' : 'en';
    storeLang(currentLang);

    var dict = T[currentLang];
    document.documentElement.lang = currentLang;
    document.title = dict["meta.title"];

    var label = document.getElementById('lang-label');
    if (label) label.textContent = currentLang === 'en' ? 'LT' : 'EN';

    applyText('[data-i18n]', 'data-i18n', function (el, value) {
        el.textContent = value;
    }, dict);
    applyText('[data-i18n-html]', 'data-i18n-html', function (el, value) {
        el.innerHTML = value;
    }, dict);
    applyText('[data-i18n-content]', 'data-i18n-content', function (el, value) {
        el.setAttribute('content', value);
    }, dict);
    applyText('[data-i18n-aria-label]', 'data-i18n-aria-label', function (el, value) {
        el.setAttribute('aria-label', value);
    }, dict);
    applyText('[data-i18n-title]', 'data-i18n-title', function (el, value) {
        el.setAttribute('title', value);
    }, dict);

    updateModeLabel(getActiveMode());
}

function toggleLang() {
    applyLang(currentLang === 'en' ? 'lt' : 'en');
}

applyLang(currentLang);
